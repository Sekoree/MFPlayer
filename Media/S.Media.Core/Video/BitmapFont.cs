    /// Returns a 128×128 RGBA texture atlas (enough for 16×9 grid of 8×13 glyphs = 144 slots).
    /// Glyphs are arranged left-to-right, top-to-bottom. White on transparent.
    /// </summary>
    public static byte[] BuildAtlasRgba(out int atlasWidth, out int atlasHeight, out int colCount)
    {
        atlasWidth = 128;
        atlasHeight = 128;
        colCount = atlasWidth / GlyphWidth; // 16
        var atlas = new byte[atlasWidth * atlasHeight * 4];

        for (int i = 0; i < GlyphCount; i++)
        {
            int col = i % colCount;
            int row = i / colCount;
            int baseX = col * GlyphWidth;
            int baseY = row * GlyphHeight;

            for (int y = 0; y < GlyphHeight; y++)
            {
                byte bits = Glyphs[i * GlyphHeight + y];
                for (int x = 0; x < GlyphWidth; x++)
                {
                    bool set = ((bits >> (7 - x)) & 1) != 0;
                    int px = ((baseY + y) * atlasWidth + (baseX + x)) * 4;
                    atlas[px + 0] = 255; // R
                    atlas[px + 1] = 255; // G
                    atlas[px + 2] = 255; // B
                    atlas[px + 3] = set ? (byte)255 : (byte)0; // A
                }
            }
        }

        return atlas;
    }

